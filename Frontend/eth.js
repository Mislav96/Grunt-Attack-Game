function getUserItems(callback){
  var prom1 = tokencontract.methods.balanceOf(account, 1 ).call();
  var prom2 = tokencontract.methods.balanceOf(account, 2 ).call();
  var prom3 = tokencontract.methods.balanceOf(account, 3 ).call();
  var prom4 = tokencontract.methods.balanceOf(account, 4 ).call();
  console.log("eth.js");
  Promise.all([prom1,prom2,prom3,prom4]).then(values => {
    if(values[0] > 0){
        console.log("has score " + values[0]);
    }
    if(values[1] > 0){
        console.log("has attack speed tokens : " + values[1]);
    }
    if(values[2] > 0){
        console.log("has movement tokens : "+ values[2] );
    }
    if(values[3] > 0){
        console.log("has bonus HP : " + values[3])
    }

    ret = {
      score : values[0],
      atspeed : values[1],
      movement : values[2],
      hp : values[3]
    }
    console.log(ret);
    callback(ret);
  });


}

